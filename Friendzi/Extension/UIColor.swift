extension UIColor {
    static func appGreenColor() -> UIColor {
        return UIColor(red:0.37, green:0.74, blue:0.35, alpha:1.00)
    }

    static func appOrangeColor() -> UIColor {
        return UIColor(red:0.93, green:0.44, blue:0.34, alpha:1.00)
    }

    static func appBlackColor() -> UIColor {
        return UIColor(red:0.20, green:0.20, blue:0.20, alpha:1.00)
    }
}
